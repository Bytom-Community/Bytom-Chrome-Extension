export default {
  components: {
    TransferConfirm
  },
  data() {
    const ASSET_BTM =
      "ffffffffffffffffffffffffffffffffffffffffffffffffffffffffffffffff";
    return {
      show: false,
      accounts: [],
      unit: "",
      assets: {
        ffffffffffffffffffffffffffffffffffffffffffffffffffffffffffffffff: "BTM"
      },
      guid: "",
      account: {
        guid: ""
      },
      transaction: {
        to: "",
        asset: ASSET_BTM,
        amount: 0,
        fee: ""
      }
    };
  },
  watch: {
    guid: function(newGuid) {
      this.accounts.forEach(account => {
        if (account.guid == newGuid) {
          this.account = account;
          return;
        }
      });
    }
  },
  methods: {
    open: function(accountInfo) {
      this.show = true;

      bytom.Account.list().then(accounts => {
        this.accounts = accounts;
      });

      this.account = accountInfo;
      this.guid = accountInfo.guid;
      this.unit = this.assets[this.transaction.asset];
    },
    close: function() {
      this.show = false;
      this.transaction.to = "";
      this.transaction.amount = 0;
    },
    send: function() {
      if (this.transaction.to == "") {
        this.$dialog.show({
          body: this.$t("transfer.emptyTo")
        });
        return;
      }
      let num = parseInt(this.transaction.amount);
      if (isNaN(num) || num <= 0) {
        this.$dialog.show({
          body: this.$t("transfer.noneBTM")
        });
        return;
      }

      let loader = this.$loading.show({
        // Optional parameters
        container: null,
        canCancel: true,
        onCancel: this.onCancel
      });
      bytom.Transaction.build(
        this.account.guid,
        this.transaction.to,
        this.transaction.asset,
        this.transaction.amount,
        this.transaction.fee
      )
        .then(ret => {
          console.log(ret);
          loader.hide();

          this.$refs.transferConfirm.open(
            this.account,
            this.transaction,
            ret.data
          );
        })
        .catch(error => {
          loader.hide();
          this.$dialog.show({
            body: getLang(error.message)
          });
        });
    }
  }
};
