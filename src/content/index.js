import bytom from "bytom-js-sdk"

//install
//npm link your_local_path_bytom-js-sdk
//npm link bytom-js-sdk
const url = 'http://127.0.0.1:9888'
const accessToken = ''

const client = new bytom(url, accessToken)

//create key
const keyPromise = client.keys.create('alias', 'password')
keyPromise.then(key => {
    console.log(key)
}).catch(function(error){
    console.log(error)
})

//accounts list
const acPromise = client.accounts.listAll()
acPromise.then(res => {
    console.log(res)
})

console.log('This is CONTENT page!')
